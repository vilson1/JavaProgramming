package day04_datatypes;

public class AreaOfSquare {
    public static void main(String[] args) {

        double square_side=21.2;
        double area= square_side*square_side;
        double perimeter = 4*square_side;

        System.out.println("square_side = " + square_side);
        System.out.println("perimeter = " + perimeter);
        System.out.println("area = " + area);


        double windth = 12.3;
        double length = 17.32;

        double area1 = length*windth;
        double perimeter1 = 2*length+2*windth;

        System.out.println("length = " + length);
        System.out.println("windth = " + windth);
        System.out.println("perimeter1 = " + perimeter1);
        System.out.println("area1 = " + area1);


    }
}
